public class UsefulNumbers {

    public static void main(String[] args) {

        int secondsInAMinute = 60;
        int secondsInAnHour = secondsInAMinute * 60;
        int secondsInADay = secondsInAnHour * 24;
        long secondsInAYear = secondsInADay * 365;
        long secondsInACentury = secondsInAYear * 100;
        System.out.println(secondsInACentury);
    }

}
